class MathHelper{
    static subtract(a,b){
        return a-b
    }
    static add(a,b){
        return a+b
    }
    static mul(a,b){
        return a*b
    }
    static div(a,b){
        return a/b
    }
}
console.log(MathHelper.subtract(45,45))
console.log(MathHelper.add(45,45))
console.log(MathHelper.mul(45,45))
console.log(MathHelper.div(45,45))